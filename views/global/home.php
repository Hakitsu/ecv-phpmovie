    <h2>Synopsis: </h2>
    <p>
        L'histoire commence lorsque, sur la Terre du Milieu, 
        le magicien Gandalf le Gris (Ian McKellen) découvre que l'anneau que possède le jeune Hobbit 
        Frodon Sacquet (Elijah Wood), qui lui a été légué par son oncle Bilbon et qui permet de se rendre 
        invisible, est en réalité l'Anneau unique recherché avidement par le seigneur ténébreux Sauron car 
        il lui permettrait de retrouver toute sa puissance. Afin d'éviter que Sauron ne récupère l'anneau, 
        Frodon et ses huit compagnons, la Communauté de l'Anneau, entament un périlleux voyage dans le but 
        de le détruire. 
    </p>

